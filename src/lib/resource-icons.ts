export function getResourceIconUrl(url: string, name: string): string {
  try {
    const domain = new URL(url).hostname;
    return `https://www.google.com/s2/favicons?domain=${domain}&sz=64&force_fallback=1`;
  } catch {
    return '';
  }
}

